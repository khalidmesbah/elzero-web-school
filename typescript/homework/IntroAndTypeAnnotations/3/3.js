function printInfo(valueOne, valueTwo) {
    return "Value One Is ".concat(valueOne, ", Value Two Is ").concat(valueTwo);
}
console.log(printInfo(10, 20)); // Value One Is 10, Value Two Is 20
console.log(printInfo("10", "20")); // Value One Is "10", Value Two Is "20"
